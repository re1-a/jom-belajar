        // Dark Mode Logic
        function toggleDarkMode() {
            const isDark = document.body.getAttribute('data-theme') === 'dark';
            if (isDark) {
                document.body.removeAttribute('data-theme');
                localStorage.setItem('theme', 'light');
                document.getElementById('btn-dark-mode').textContent = '🌙';
            } else {
                document.body.setAttribute('data-theme', 'dark');
                localStorage.setItem('theme', 'dark');
                document.getElementById('btn-dark-mode').textContent = '☀️';
            }
        }

        // Initialize Theme
        if (localStorage.getItem('theme') === 'dark' || (!localStorage.getItem('theme') && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
            document.body.setAttribute('data-theme', 'dark');
            document.getElementById('btn-dark-mode').textContent = '☀️';
        }

        // UI State
        let currentMode = 'browse';
        let currentTopic = 'adabmakan';
        let currentQuizIndex = 0;
        let score = 0;
        
        let quizState = { topicScores: {} };

        // Elements
        const browseView = document.getElementById('browse-view');
        const quizView = document.getElementById('quiz-view');
        const progressView = document.getElementById('progress-view');
        const quizContainer = document.getElementById('quiz-container');
        
        // Tab Switching
        document.getElementById('tab-browse').addEventListener('click', () => setMode('browse'));
        document.getElementById('tab-quiz').addEventListener('click', () => setMode('quiz'));
        document.getElementById('tab-progress').addEventListener('click', () => setMode('progress'));

        // Topic Switching
        document.querySelectorAll('.topic-pill').forEach(pill => {
            pill.addEventListener('click', (e) => {
                document.querySelectorAll('.topic-pill').forEach(p => p.classList.remove('active'));
                e.target.classList.add('active');
                currentTopic = e.target.getAttribute('data-topic');
                renderContent();
            });
        });

        function setMode(mode) {
            currentMode = mode;
            document.getElementById('tab-browse').classList.toggle('active', mode === 'browse');
            document.getElementById('tab-quiz').classList.toggle('active', mode === 'quiz');
            document.getElementById('tab-progress').classList.toggle('active', mode === 'progress');
            
            browseView.classList.add('hidden');
            quizView.classList.add('hidden');
            progressView.classList.add('hidden');
            
            if (mode === 'browse') {
                browseView.classList.remove('hidden');
                document.getElementById('topic-filters').classList.remove('hidden');
            } else if (mode === 'quiz') {
                quizView.classList.remove('hidden');
                document.getElementById('topic-filters').classList.remove('hidden');
                startQuiz();
            } else if (mode === 'progress') {
                progressView.classList.remove('hidden');
                document.getElementById('topic-filters').classList.add('hidden');
                renderProgressView();
            }
            renderContent();
        }

        function renderContent() {
            if (currentMode === 'browse') {
                browseView.innerHTML = notaData[currentTopic];
            }
        }

        function startQuiz() {
            currentQuizIndex = 0;
            score = 0;
            renderQuizQuestion();
        }

        function renderQuizQuestion() {
            const questions = quizData[currentTopic];
            if (currentQuizIndex >= questions.length) {
                // Quiz complete
                quizContainer.innerHTML = `
                    <div class="quiz-card">
                        <h2>Kuiz Tamat!</h2>
                        <div style="font-size: 48px; color: #059669; font-weight: bold;">${score} / ${questions.length}</div>
                        <button class="quiz-next-btn show" onclick="startQuiz()" style="margin-top: 20px;">Cuba Lagi</button>
                    </div>
                `;
                return;
            }

            const q = questions[currentQuizIndex];
            let optionsHtml = '';
            const labels = ['A', 'B', 'C', 'D'];
            q.options.forEach((opt, idx) => {
                optionsHtml += `<div class="quiz-option" onclick="selectAnswer(${idx}, ${q.answer})"><strong>${labels[idx]}.</strong> ${opt}</div>`;
            });

            quizContainer.innerHTML = `
                <div class="quiz-card">
                    <div style="text-align: left; color: #718096; margin-bottom: 10px;">Soalan ${currentQuizIndex + 1} daripada ${questions.length}</div>
                    <div class="quiz-question">${q.q}</div>
                    <div class="quiz-options">
                        ${optionsHtml}
                    </div>
                    <div id="explanation-box" style="display:none; margin-top:15px; padding:12px; border-radius:8px; background-color:#fee2e2; color:#991b1b; font-size:14px; border-left:4px solid #ef4444; line-height:1.5;"></div>
                    <button class="quiz-next-btn" id="next-btn" onclick="nextQuestion()">Seterusnya ➡️</button>
                </div>
            `;
        }

        window.selectAnswer = function(selectedIdx, correctIdx) {
            const options = document.querySelectorAll('.quiz-option');
            if (options[0].classList.contains('disabled')) return; // already answered

            const q = quizData[currentTopic][currentQuizIndex];

            options.forEach((opt, idx) => {
                opt.classList.add('disabled');
                if (idx === correctIdx) {
                    opt.classList.add('correct');
                } else if (idx === selectedIdx && selectedIdx !== correctIdx) {
                    opt.classList.add('wrong');
                }
            });

            if (!quizState.topicScores[currentTopic]) {
                quizState.topicScores[currentTopic] = { correct: 0, total: 0 };
            }
            quizState.topicScores[currentTopic].total++;

            if (selectedIdx === correctIdx) {
                score++;
                quizState.topicScores[currentTopic].correct++;
            } else {
                if (q.explanation) {
                    const explBox = document.getElementById('explanation-box');
                    explBox.style.display = 'block';
                    explBox.innerHTML = '<strong>💡 Tahukah anda?</strong><br>' + q.explanation;
                }
            }

            saveProgress();
            document.getElementById('next-btn').classList.add('show');
        }

        window.nextQuestion = function() {
            currentQuizIndex++;
            renderQuizQuestion();
        }

        function loadProgress() {
            const saved = localStorage.getItem('kafa_akhlak_progress');
            if (saved) {
                try {
                    quizState.topicScores = JSON.parse(saved);
                } catch(e) {}
            }
        }

        function saveProgress() {
            localStorage.setItem('kafa_akhlak_progress', JSON.stringify(quizState.topicScores));
        }

        function renderProgressView() {
            loadProgress();
            const scores = quizState.topicScores;
            const topics = ['adabmakan', 'adabtidur', 'adabberkenderaan', 'adabmasjid', 'adabbelajar', 'adabalquran', 'adabhartaawam'];
            const topicNames = {
                'adabmakan': 'Adab Makan & Minum',
                'adabtidur': 'Adab Tidur',
                'adabberkenderaan': 'Adab Berkenderaan',
                'adabmasjid': 'Adab di Masjid',
                'adabbelajar': 'Adab Belajar',
                'adabalquran': 'Adab dengan Al-Quran',
                'adabhartaawam': 'Harta Awam'
            };

            let totalCorrect = 0;
            let totalAttempted = 0;
            let html = '<div class="progress-container">';

            let cardsHtml = '';
            topics.forEach(t => {
                const s = scores[t] || { correct: 0, total: 0 };
                totalCorrect += s.correct;
                totalAttempted += s.total;
                
                let pct = 0;
                if (s.total > 0) pct = Math.round((s.correct / s.total) * 100);
                
                cardsHtml += `
                    <div class="progress-card">
                        <div class="progress-info">
                            <div class="progress-title">${topicNames[t]}</div>
                            <div class="progress-bar-track">
                                <div class="progress-bar-value" style="width: ${pct}%;"></div>
                            </div>
                        </div>
                        <div class="progress-stats">${pct}%</div>
                    </div>
                `;
            });

            let overallPct = 0;
            if (totalAttempted > 0) overallPct = Math.round((totalCorrect / totalAttempted) * 100);

            html += `
                <div class="progress-total">
                    <h3>Pencapaian Keseluruhan</h3>
                    <div class="big-score">${overallPct}%</div>
                    <div style="color: var(--text-light); font-size: 14px;">${totalCorrect} betul daripada ${totalAttempted} cubaan</div>
                </div>
            `;
            
            html += cardsHtml;
            
            if (totalAttempted > 0) {
                html += `<button class="reset-btn" onclick="resetProgress()">Set Semula Rekod</button>`;
            } else {
                html += `<div style="text-align:center; color:var(--text-light); margin-top:20px;">Belum ada sebarang rekod kuiz dijawab.</div>`;
            }

            html += '</div>';
            progressView.innerHTML = html;
        }

        window.resetProgress = function() {
            if(confirm('Anda pasti mahu memadam semua rekod pencapaian Akhlak?')) {
                localStorage.removeItem('kafa_akhlak_progress');
                quizState.topicScores = {};
                renderProgressView();
            }
        }

        // Initialize
        loadProgress();
        renderContent();
